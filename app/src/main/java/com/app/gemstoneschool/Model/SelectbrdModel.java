package com.app.gemstoneschool.Model;

public class SelectbrdModel {

    private String name,id;

    public SelectbrdModel(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return name;
    }///for showing name in spinner as items
}
